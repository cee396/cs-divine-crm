export async function seedDatabase() {
  try {
    // Create default users
    const adminExists = await prisma.user.findUnique({
      where: { email: 'admin@taxdeedcrm.com' }
    });

    if (!adminExists) {
      await prisma.user.create({
        data: {
          email: 'admin@taxdeedcrm.com',
          name: 'Admin User',
          password: await hashPassword('admin123'),
          role: 'admin'
        }
      });
    }

    const userExists = await prisma.user.findUnique({
      where: { email: 'user@taxdeedcrm.com' }
    });

    if (!userExists) {
      await prisma.user.create({
        data: {
          email: 'user@taxdeedcrm.com',
          name: 'CRM User',
          password: await hashPassword('user123'),
          role: 'user'
        }
      });
    }

    // Seed properties
    for (const property of FLORIDA_PROPERTIES) {
      const exists = await prisma.property.findUnique({
        where: { parcelId: property.parcelId }
      });

      if (!exists) {
        await prisma.property.create({
          data: property
        });
      }
    }

    console.log('Database seeded successfully!');
  } catch (error) {
    console.error('Error seeding database:', error);
  }
}
